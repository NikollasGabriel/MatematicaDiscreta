    private void listaConjuntosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaConjuntosMouseClicked
        if (conjunto1.getText().isEmpty()) {
            conjunto1.setText(listaConjuntos.getSelectedValue());
        } else if (!(conjunto1.getText().isEmpty()) && conjunto2.getText().isEmpty()) {
            conjunto2.setText(listaConjuntos.getSelectedValue());
        } else if (!(conjunto2.getText().isEmpty()) && conjunto3.isEnabled()) {
            conjunto3.setText(listaConjuntos.getSelectedValue());
        }
    }//GEN-LAST:event_listaConjuntosMouseClicked

    private void relacaoFuncionalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_relacaoFuncionalActionPerformed

        String operacao = "";

        if (operacoes.getSelectedItem().toString().equals("x² = y")) {
            operacao = "quadrado";
        } else if (operacoes.getSelectedItem().toString().equals("√x = y")) {
            operacao = "raizQuadrada";
        } else {
            operacao = operacoes.getSelectedItem().toString();
        }

        if ((conjunto3.isEnabled()) && campoVazioComposto()) {

            Relacao relacao = new Relacao(conjunto1.getText(), conjunto2.getText(), conjunto3.getText(), operacao);
            Funcional.relacaoFuncionalComposta(relacao);
        } else if (campoVazio()) {

            Relacao relacao = new Relacao(conjunto1.getText(), conjunto2.getText(), operacao);
            Funcional.relacaoFuncional(relacao);
        }
    }//GEN-LAST:event_relacaoFuncionalActionPerformed

