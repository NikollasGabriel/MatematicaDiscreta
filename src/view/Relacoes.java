    private boolean campoVazio() {

        if (conjunto1.getText().equals("") && conjunto2.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Um ou mais campos estão vazios!");
            return false;
        } else if (conjunto1.getText().equals("") || conjunto2.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Um campo esta vazio!");
            return false;
        } else {
            return true;
        }

    }

    private boolean campoVazioComposto() {

        if (conjunto1.getText().equals("") && conjunto2.getText().equals("") && conjunto3.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Um ou mais campos estão vazios!");
            return false;

        } else if (conjunto1.getText().equals("") || conjunto2.getText().equals("") || conjunto3.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Um campo esta vazio!");
            return false;
        } else {
            return true;
        }

    }

